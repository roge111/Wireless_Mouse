#!/usr/bin/env python3
"""
Тестовый скрипт для проверки сенсора касания на Raspberry Pi.
Скрипт непрерывно считывает состояние сенсора на GPIO 17 и выводит
сообщение при касании.

Используется для отладки подключения сенсора касания в проекте "Воздушная мышь".

Автор: Студенты ИТМО, курс "Интернет вещей"
Версия: 1.0
Дата: 2025
"""

import RPi.GPIO as GPIO
import time

# Настройка нумерации пинов (BCM)
GPIO.setmode(GPIO.BCM)

# Пин подключения сенсора касания
TOUCH_PIN = 17

# Настройка пина как входа с внутренней подтяжкой к питанию
GPIO.setup(TOUCH_PIN, GPIO.IN, pull_up_down=GPIO.PUD_UP)

print("Тест сенсора касания запущен. Нажмите Ctrl+C для выхода.")
print("Сенсор подключен к GPIO 17 (BCM).")

try:
    while True:
        time.sleep(0.01)  # Небольшая задержка для снижения нагрузки на CPU
        
        # Чтение состояния сенсора
        touch_state = GPIO.input(TOUCH_PIN)
        
        # Логика сенсора: LOW (0) - не касаются, HIGH (1) - касаются
        if touch_state == GPIO.LOW:
            print("Сенсор касания: не активирован.")
        else:
            print("Сенсор касания: АКТИВИРОВАН!")
        
except KeyboardInterrupt:
    # Очистка настроек GPIO при завершении программы
    print("\nЗавершение работы...")
    GPIO.cleanup()
    print("GPIO очищены.")
